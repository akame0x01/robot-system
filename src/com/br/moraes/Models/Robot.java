package com.br.moraes.Models;

public class Robot {
    private Integer id;
    private String size;
    private String weight;
    private String stats;
    private String color;
    private String age;
    private String numberOfEyes;
    private String numberOfAntennas;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getStats() {
        return stats;
    }

    public void setStats(String stats) {
        this.stats = stats;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getNumberOfEyes() {
        return numberOfEyes;
    }

    public void setNumberOfEyes(String numberOfEyes) {
        this.numberOfEyes = numberOfEyes;
    }

    public String getNumberOfAntennas() {
        return numberOfAntennas;
    }

    public void setNumberOfAntennas(String numberOfAntennas) {
        this.numberOfAntennas = numberOfAntennas;
    }

}
